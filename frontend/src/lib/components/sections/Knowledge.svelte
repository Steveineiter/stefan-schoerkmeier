    <h3>Health & Lifestyle</h3>
    <p>
      Health is the most important thing we can have - most people would agree. Unfortunately, there’s so much
      misinformation out there, and many people find topics like diet confusing (myself included). I’ve tried many diets - keto, carnivore, Mediterranean, vegan, and more.
      What’s interesting is that we’ve known the rough steps to a healthy diet since the 1980s. Of course, if you have allergies or specific illnesses, these findings might not apply to you.
      But for 90% of the population, they do. According to
      <a href="https://www.youtube.com/watch?v=h2q5qC7NLMY" target="_blank">Dr. Gardner</a>, the 7 key principles are:
    </p>
    <ol>
      <li>Eat a variety of foods</li>
      <li>Maintain an ideal weight</li>
      <li>Avoid too much fat, saturated fat, and cholesterol</li>
      <li>Eat foods with adequate starch and fiber</li>
      <li>Avoid too much sugar</li>
      <li>Avoid too much salt</li>
      <li>If you drink alcohol, do so in moderation</li>
    </ol>
    <p>
      However, <a href="https://www.youtube.com/watch?v=5s2U4GGBZak" target="_blank">newer research</a> suggests that alcohol is harmful even in low doses.
      The cool part about these 7 principles is that they make it easy to determine whether something is healthy or not.
      For example, eating a Wiener Schnitzel with fries, dessert, and a beer every day? That’s unhealthy because it breaks almost all the principles above (unless the portions are tiny and the rest of your diet is stellar).
      We can even quantify this: sugar should be no more than 5% of your daily calories, saturated fats no more than 10%, and salt no more than 2.3 grams.
      For someone consuming 2,500 calories per day (the average male), this translates to around 32g of sugar and 27g of saturated fat.
    </p>
    <p>
      Personally, I find a <b>plant-based whole food diet</b> with 1–2 portions of meat or fish per week works best for me.
      This approach naturally aligns with the 7 principles while still being flexible enough to enjoy dining out with friends.
    </p>
    <p>
      Beyond diet, <b>sleep and exercise</b> are crucial for a healthy life. In a nutshell, aim for 7–9 hours of high-quality sleep,
      2–3 hours of weightlifting, and 2–3 hours of moderate cardio per week for optimal health.
      I’d also add <b>ergonomics</b> (workspace and sleep setup) and <b>stress management</b> (e.g., meditation, yoga nidra, progressive muscle relaxation).
    </p>
